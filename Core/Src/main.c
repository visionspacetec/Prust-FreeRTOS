int prust_recv_packet(uint8_t * dest, size_t dest_cap, uint32_t timeout){
	static const uint8_t header_len = 6;

	size_t data_len;
	int res = 0;
	{
		if (HAL_OK != HAL_UART_Receive(&huart5, dest, header_len, timeout)){
			res = -1;
		} else{
			data_len = ((dest[4])<<8) + dest[5] + 1;
			if (dest_cap > (data_len + header_len) &&
					HAL_OK != HAL_UART_Receive(&huart5, dest+header_len, data_len, timeout)){
				res = -1;
			}else{
				res = 0;
			}
		}
	}
	osThreadYield();
	return res;
}

int prust_send_packet(uint8_t * src, size_t src_len, uint32_t timeout){
	int res = 0;
	vPortEnterCritical();
	vTaskSuspendAll();
	{
		if (HAL_OK != HAL_UART_Transmit(&huart5, src, src_len, timeout)){
			res = -1;
		} else{
			res = 0;
		}
	}
	xTaskResumeAll();
	vPortExitCritical();
	return res;
}
